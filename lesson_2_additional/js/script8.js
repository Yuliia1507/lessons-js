
/*     Задача 8
Розробити програму (не використовувати if), яка за номером року Y визначає номер століття C (врахувати, що початком XX століття був 1901, а не 1900 рік).
*/

// Вводимо дані

let yearY = parseInt(prompt('Введіть рік Y', '0'))	

// Обчислення результату

let centuryC = Math.ceil(yearY / 100)

// let centuryC = Math.floor((year - 1) / 100) + 1;


// Виведення результату

document.write(`
Рік ${yearY} належить до ${centuryC} століття.
`)
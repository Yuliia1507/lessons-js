/*З клавіатури вводиться кількість монет номіналом 1 копійка. Визначити
скільки гривень і копійок є у даній сумі .
Наприклад, дано 245 копійок, а вивести 2 грн 45 копійок. */

//Введення даних

let coinsNumber = parseInt(prompt('Введіть кількість монет','0'))

//Обчислення результату


let hryvnas = Math.trunc(coinsNumber / 100) 

let coins = coinsNumber %  100

//Виведення даних

document.write(`${coinsNumber} копійок це ${hryvnas}грн ${coins}коп`)